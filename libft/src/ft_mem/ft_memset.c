#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	s;
	char			*str;
	size_t			i;

	s = (unsigned char) c;
	i = 0;
	str = (char *) b;
	while (i < len)
		str[i++] = s;
	b = (void *) str;
	return (b);
}
